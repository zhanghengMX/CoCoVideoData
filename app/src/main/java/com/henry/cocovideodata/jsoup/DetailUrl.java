public class DetailUrl {

    private String title;
    private String time;
    private String url;



    public String getTitle() {
        return title;
    }

    public DetailUrl setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getTime() {
        return time;
    }

    public DetailUrl setTime(String time) {
        this.time = time;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public DetailUrl setUrl(String url) {
        this.url = url;
        return this;
    }
}
